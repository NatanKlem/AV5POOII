package model;

public class Motoristas {
    
    int id_motoristas;
    String nome_motoristas;
    String nascimento_motoristas;
    String cnh_motoristas;

    public int getId_motoristas() {
        return id_motoristas;
    }

    public void setId_motoristas(int id_motoristas) {
        this.id_motoristas = id_motoristas;
    }

    public String getNome_motoristas() {
        return nome_motoristas;
    }

    public void setNome_motoristas(String nome_motoristas) {
        this.nome_motoristas = nome_motoristas;
    }

    public String getNascimento_motoristas() {
        return nascimento_motoristas;
    }

    public void setNascimento_motoristas(String nascimento_motoristas) {
        this.nascimento_motoristas = nascimento_motoristas;
    }

    public String getCnh_motoristas() {
        return cnh_motoristas;
    }

    public void setCnh_motoristas(String cnh_motoristas) {
        this.cnh_motoristas = cnh_motoristas;
    }
    
    
}
