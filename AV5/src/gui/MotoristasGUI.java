    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Motoristas motoristas = new Motoristas();
        motoristas.setNome_motoristas(txtNomeMotoristas.getText());
        motoristas.setNascimento_motoristas(txtNascimentoMotoristas.getText());
        motoristas.setCnh_motoristas(txtCnhMotoristas.getText());
        
        if (txtNomeMotoristas.getText().isEmpty() || 
            txtNascimentoMotoristas.getText().isEmpty()  || 
            txtCnhMotoristas.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
       
            
        }
        else{
            
            MotoristasDAO dao = new MotoristasDAO();
            dao.inserir(motoristas);
            
             JOptionPane.showMessageDialog(null, "Motorista "+ txtNomeMotoristas.getText()
                    +" foi inserido com sucesso"); 
            
        }
        
          txtNomeMotoristas.setText("");
          txtNascimentoMotoristas.setText("");
          txtCnhMotoristas.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(txtIdMotoristas.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Digite o ID do Motorista");
            
        }
        
        else{
            
            try {
                
              Class.forName("com.mysql.cj.jdbc.Driver");
               
               Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/av5?useTimezone=true&serverTimezone=UTC","root","");
               
               java.sql.Statement st = conn.createStatement();
               st.executeUpdate("delete from motoristas where id_motoristas = ?  + this.txtIdMostoristas.getText()");
               JOptionPane.showMessageDialog(rootPane, "Motorista excluído com sucesso!");
              
                
            } catch (ClassNotFoundException e) {
               JOptionPane.showMessageDialog(rootPane, e);  
                
            } catch (SQLException ex) {
                Logger.getLogger(MotoristasGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
             
             txtNomeMotoristas.setText("");
             txtNascimentoMotoristas.setText("");
             txtCnhMotoristas.setText("");
        
        } 
    }//GEN-LAST:event_btnExcluirActionPerformed

