    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
         txtNomeMotoristas.setEnabled(true);
         txtNascimentoMotoristas.setEnabled(true);
         txtCnhMotoristas.setEnabled(true);      
         btnLimpar.setEnabled(true);
         btnExcluir.setEnabled(true);
         btnAtualizar.setEnabled(true);
         
        if (txtIdMotoristas.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(null, "Digite o ID do Motorista");
            
            
            
        } else {
            
            
            Motoristas motoristas = new Motoristas();
            motoristas.setId_motoristas(Integer.parseInt(txtIdMotoristas.getText()));
            
            MotoristasDAO dao = new MotoristasDAO();
            motoristas = dao.consultar(motoristas);
            
            if(motoristas != null){
                
                txtNomeMotoristas.setText(motoristas.getNome_motoristas());
                txtNascimentoMotoristas.setText(motoristas.getNascimento_motoristas());
                txtCnhMotoristas.setText(motoristas.getCnh_motoristas());
                
                
                
                
            }
            
            else{
                
               JOptionPane.showMessageDialog(null, "ID Inválido");
            
                
            }
            
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

